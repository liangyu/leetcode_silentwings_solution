package LC2101_2400;

public class LC2235_AddTwoIntegers {
    /**
     * Given two integers num1 and num2, return the sum of the two integers.
     *
     * Input: num1 = 12, num2 = 5
     * Output: 17
     *
     * Constraints:
     *
     * -100 <= num1, num2 <= 100
     * @param num1
     * @param num2
     * @return
     */
    // time = O(1), space = O(1)
    public int sum(int num1, int num2) {
        return num1 + num2;
    }
}
